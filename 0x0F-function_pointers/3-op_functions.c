#include "3-calc.h"

/**
 * op_add - adds arguments
 * @a: argument
 * @b: argument
 * Return: returns sum
 */

int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - substracts arguments
 * @a: argument
 * @b: argument
 * Return: returns diff
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiplies arguments
 * @a: argument
 * @b: argument
 * Return: returns product
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divides arguments
 * @a: argument
 * @b: argument
 * Return: returns division
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - returns remainder of arguments
 * @a: argument
 * @b: argument
 * Return: returns remainder
 */
int op_mod(int a, int b)
{
	return (a % b);
}
